#include "pipex.h"

void	ft_bzero(void *s, size_t n)
{
	char	*pointer;
	size_t	i;

	i = 0;
	pointer = (char *) s;
	while (i < n)
	{
		pointer[i] = 0;
		i++;
	}
}
